// string concatenation
let message = 'Hello';
message = message + ' World';
console.log(message);

// using string and method (lowercase)
let message2 = 'Hello';
message2 = message2.toLowerCase();
console.log(message2);

// using string and method (uppercase)
let message3 = 'Hello';
message3 = message3.toUpperCase();
console.log(message3);

// using string and property (length)
let message4 = 'Hello';
message4 = message4.length;
console.log(message4);